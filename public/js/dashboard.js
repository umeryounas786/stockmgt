function getToken() {
    return localStorage.getItem('token');
}

function getAuthHeaders() {
    const headers = { 'Content-Type': 'application/json' };
    const token = getToken();
    if (token) {
        headers['Authorization'] = `Bearer ${token}`;
    }
    return headers;
}

function escapeHtml(value) {
    if (value === null || value === undefined) return '';
    return String(value)
        .replace(/&/g, '&amp;')
        .replace(/</g, '&lt;')
        .replace(/>/g, '&gt;')
        .replace(/"/g, '&quot;')
        .replace(/'/g, '&#039;');
}

async function loadCompanyOptions() {
    try {
        const response = await fetch('/api/companies', { headers: getAuthHeaders() });

        if (response.status === 401) {
            window.location.href = '/login';
            return;
        }
        if (!response.ok) throw new Error('Failed to load companies');

        const companies = await response.json();
        const select = document.getElementById('companyId');
        select.innerHTML = '<option value="">-- Select Company --</option>';
        companies.forEach(company => {
            const opt = document.createElement('option');
            opt.value = company.company_id;
            opt.textContent = `${company.company_name} (${company.company_code})`;
            select.appendChild(opt);
        });
    } catch (error) {
        console.error('Error loading companies:', error);
        alert('Error loading companies: ' + error.message);
    }
}

async function loadProducts() {
    try {
        const response = await fetch('/api/products', { headers: getAuthHeaders() });

        if (response.status === 401) {
            window.location.href = '/login';
            return;
        }
        if (!response.ok) throw new Error('Failed to load products');

        const products = await response.json();
        displayProducts(products);
    } catch (error) {
        console.error('Error loading products:', error);
        alert('Error loading products: ' + error.message);
    }
}

function displayProducts(products) {
    const tableBody = document.getElementById('productTableBody');
    tableBody.innerHTML = '';

    if (!products || products.length === 0) {
        tableBody.innerHTML = '<tr><td colspan="9" style="text-align: center; padding: 20px;">No products found</td></tr>';
        return;
    }

    products.forEach(product => {
        const status = product.stock_in_hand <= 0 ? 'critical' : 'ok';
        const row = document.createElement('tr');
        row.innerHTML = `
            <td><strong>${escapeHtml(product.product_code)}</strong></td>
            <td>${escapeHtml(product.product_description) || '-'}</td>
            <td>${escapeHtml(product.company_name)}</td>
            <td>${product.product_packet_size}</td>
            <td>${product.product_stock_purchase}</td>
            <td>${product.total_sale}</td>
            <td><span class="status-badge status-${status}">${product.stock_in_hand}</span></td>
            <td>${Number(product.stock_in_boxes)}</td>
            <td>
                <div class="action-buttons">
                    <button class="btn btn-edit" onclick="editProduct(${product.product_id})">Edit</button>
                    <button class="btn btn-danger" onclick="deleteProduct(${product.product_id})">Delete</button>
                </div>
            </td>
        `;
        tableBody.appendChild(row);
    });
}

function openAddProductModal() {
    document.getElementById('productForm').reset();
    document.getElementById('productModalTitle').textContent = 'Add Product';
    document.getElementById('productForm').dataset.productId = '';
    // Company is selectable on add; product code is generated by the server.
    document.getElementById('companyId').disabled = false;
    document.getElementById('productCode').value = '';
    document.getElementById('productModal').classList.add('show');
}

async function editProduct(productId) {
    try {
        const response = await fetch(`/api/products/${productId}`, { headers: getAuthHeaders() });
        if (!response.ok) throw new Error('Failed to load product');

        const product = await response.json();

        document.getElementById('companyId').value = product.company_id || '';
        document.getElementById('productCode').value = product.product_code || '';
        document.getElementById('description').value = product.product_description || '';
        document.getElementById('packetSize').value = product.product_packet_size;
        document.getElementById('stockPurchase').value = product.product_stock_purchase;

        // Company and product code are fixed once a product exists.
        document.getElementById('companyId').disabled = true;

        document.getElementById('productModalTitle').textContent = 'Edit Product';
        document.getElementById('productForm').dataset.productId = productId;
        document.getElementById('productModal').classList.add('show');
    } catch (error) {
        console.error('Error editing product:', error);
        alert('Error loading product: ' + error.message);
    }
}

async function deleteProduct(productId) {
    if (!confirm('Are you sure you want to delete this product? Its sales records will also be removed.')) return;

    try {
        const response = await fetch(`/api/products/${productId}`, {
            method: 'DELETE',
            headers: getAuthHeaders()
        });

        const body = await response.json().catch(() => ({}));
        if (!response.ok) {
            alert(body.message || 'Failed to delete product');
            return;
        }

        alert('Product deleted successfully');
        loadProducts();
    } catch (error) {
        console.error('Error deleting product:', error);
        alert('Error deleting product: ' + error.message);
    }
}

document.getElementById('productForm').addEventListener('submit', async (e) => {
    e.preventDefault();

    const productId = document.getElementById('productForm').dataset.productId;
    const companyIdValue = document.getElementById('companyId').value;
    if (!companyIdValue) {
        alert('Please select a company');
        return;
    }

    const data = {
        description: document.getElementById('description').value.trim(),
        packetSize: parseInt(document.getElementById('packetSize').value, 10),
        stockPurchase: parseInt(document.getElementById('stockPurchase').value, 10)
    };
    // Company is only sent on create; on edit it is fixed server-side.
    if (!productId) {
        data.companyId = parseInt(companyIdValue, 10);
    }

    try {
        const url = productId ? `/api/products/${productId}` : '/api/products';
        const method = productId ? 'PUT' : 'POST';

        const response = await fetch(url, {
            method,
            headers: getAuthHeaders(),
            body: JSON.stringify(data)
        });

        const body = await response.json().catch(() => ({}));
        if (!response.ok) {
            alert(body.message || 'Failed to save product');
            return;
        }

        alert(productId ? 'Product updated successfully' : 'Product created successfully');
        document.getElementById('productModal').classList.remove('show');
        loadProducts();
    } catch (error) {
        console.error('Error saving product:', error);
        alert('Error saving product: ' + error.message);
    }
});

// When adding a product, preview the code that will be generated for the
// chosen company (e.g. company 'sam' -> 'sam1'). The field stays read-only.
document.getElementById('companyId').addEventListener('change', async (e) => {
    if (document.getElementById('productForm').dataset.productId) return;

    const companyId = e.target.value;
    const codeField = document.getElementById('productCode');
    if (!companyId) {
        codeField.value = '';
        return;
    }

    try {
        const response = await fetch(`/api/products/next-code/${companyId}`, { headers: getAuthHeaders() });
        if (!response.ok) throw new Error('Failed to get next product code');
        const body = await response.json();
        codeField.value = body.productCode || '';
    } catch (error) {
        console.error('Error fetching next product code:', error);
        codeField.value = '';
    }
});

document.getElementById('addProductBtn').addEventListener('click', openAddProductModal);
document.getElementById('productCancelBtn').addEventListener('click', () => {
    document.getElementById('productModal').classList.remove('show');
});
document.querySelector('.close').addEventListener('click', () => {
    document.getElementById('productModal').classList.remove('show');
});

window.addEventListener('click', (e) => {
    const modal = document.getElementById('productModal');
    if (e.target === modal) {
        modal.classList.remove('show');
    }
});

document.addEventListener('DOMContentLoaded', () => {
    loadCompanyOptions();
    loadProducts();
});
